// Fonction qui ajoute 1 à son argument
function more(num) {
    return num + 1;
}

// Fonction qui soustrait 1 à son argument
function less(num) {
    return num - 1;
}

// Fonction qui additionne deux nombres
function add(a, b) {
    return a + b;
}

// Fonction qui soustrait le deuxième argument du premier
function sub(a, b) {
    return a - b;
}